FRAME_TYPES = {
    0x00: "GROUP_SHOT",
    0x02: "GROUP_SHOT_CHARCTR_TALK",
    0x03: "CLOSEUP",
    0x04: "CLOSEUP_CONTINUATION",
}

def exctract_convs(conv_bytes) :
    size = len(conv_bytes)
    read = 0
    convs = []
    convs_size = 0
    end_sequence = [0xFF, 0xFF]
    between_conv = False
    between_bytes = []
    conv_offset = 0
    while read < size :
        if not between_conv:
            if convs_size == 0:
                conv_offset = read
                convs_chunk = []
            convs_chunk.append(conv_bytes[read])
            convs_size += 1
            read += 1
            if len(convs_chunk) >= 2 and convs_chunk[-2:] == end_sequence:
                convs_size = 0
                between_conv = True
                between_bytes = []
        else:
            if conv_bytes[read] in FRAME_TYPES.keys() and len(between_bytes) >= 2 and conv_bytes[read+1] not in FRAME_TYPES.keys():
                convs.append((convs_chunk, between_bytes, conv_offset))
                between_conv = False
                between_bytes = []
            else:
                between_bytes.append(conv_bytes[read])
                read += 1
    return convs

def parse_frame_GROUP_SHOT(bytes):
    
    settings = ''.join(chr(b) for b in bytes[1:8])
    offset = 9
    # look for 0x0A 0x0A indicating the start of the text
    before_text_bytes = []
    while not (bytes [offset] == 0x0A):
        offset +=1
        before_text_bytes.append(bytes [offset])
    text = ""
    for i in range(offset+2, len(bytes), 1):
        if bytes[i] == 0x00:
            break
        text += chr(bytes[i])
    remaining_bytes = bytes[i+1:]
    print(f"{' '*4}Parsing GROUP_SHOT frame")
    print(f"{' '*4}Settings bytes: {settings}")
    print(f"{' '*4}Before text bytes: {before_text_bytes}")
    print(f"{' '*4}Text: {text}")
    print(f"{' '*4}Remaining bytes: {remaining_bytes}")


def parse_frame_CLOSEUP(bytes):
    actor = ''.join(chr(b) for b in bytes[1:9] if b != 0x00)
    setting = ''.join(chr(b) for b in bytes[11:18] if b != 0x00)
    x = bytes[20]
    y = bytes[21]
    text = ""
    offset = 24
    for i in range(24, len(bytes), 1):
        if bytes[i] == 0x00:
            offset += 1
            break
        text += chr(bytes[i])
        offset += 1
    remaining_bytes = bytes[offset:]
    print(f"{' '*4}Parsing CLOSEUP frame")
    print(f"{' '*4}Actor: {actor}, Setting: {setting}, X: {x}, Y: {y}")
    print(f"{' '*4}Text: {text}")
    print(f"{' '*4}Remaining bytes: {remaining_bytes}")

